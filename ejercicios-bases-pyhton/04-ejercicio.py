"""

Pedir dos numeros al usuario y hacer todas las operaciones basicas
de calculadora, mostrarlo en pantalla.

"""

num1 = int(input("Ingresa el primer numero: "))
num2 = int(input("Ingresa el segundo numero: "))

print("#### Calculadora ####")
print(f"La suma de los numeros es: {num1 + num2}")
print(f"La resta de los numeros es: {num1 - num2}")
print(f"La multiplicaion de los numeros es: {num1 * num2}")
print(f"La division de los numeros es: {num1 / num2}")